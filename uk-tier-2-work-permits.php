                  <div id="There Are 4 Categories Of Skilled Worker Under Tier 2 (UK Work Permit / UK Work Visa)" class="tabcontent">
                    <h3>There Are 4 Categories Of Skilled Worker Under Tier 2 (UK Work Permit / UK Work Visa)</h3>
                     <p>
                       <b>Tier 2 – General</b><br>

                        The Tier 2 (UK Work Permit / UK Work Visa) is for qualified professionals to come to the UK with a skilled job offer to fill a gap in the workforce that cannot be filled by a settled worker. It is an employer based procedure wherein the sponsoring company will apply on behalf of the applicant. However, the candidates are required to pass a points test. To qualify candidates are required to satisfy the minimum pass mark of 70 points.

                        <b>Points Criteria - Candidates</b><br>

                        A total of 70 points must be scored across various criteria prior to assigning a sponsorship certificate.<br>

                        You must score a minimum of:<br>

                       <li> 50 points for Attributes, which includes having a sponsor and a valid Certificate of Sponsorship; and</li>
                       <li> 10 points for English language skills(except for certain Intra-Company Transfers); and</li>
                       <li> 10 points for Maintenance funds</li>
                        <b>Employer Requirements for Tier 2 (UK Work Permit / UK Work Visa)</b><br>

                        The candidates, in addition to meeting the required points should have a job offer/sponsor and a certificate of sponsorship. The responsibility is on the employer to ensure that the candidates are eligible and suitable to fill the positions being offered in the organisation.

                        <b>Basic Features of the Tier 2 visa (UK Work Permit / UK Work Visa)</b><br>

                        <li>Tier 2 visas (UK Work Permit / UK Work Visa) are not a permanent residency visa in itself, but it is a route to permanent residency in the United Kingdom. After 5 years in the UK, applicants can apply for Indefinite Leave to Remain or settlement in the UK (ILR). Further to this, an applicant may opt to undergo British Naturalization as a UK citizen.</li>
                        <li>Tier 2 (UK Work Permit / UK Work Visa) applicants can change their job while being in the United Kingdom, although the process of attaining a Certificate of Sponsorship must be repeated and applicants should be reassessed against the points test.</li><br>
                        <b>Duration of the Tier 2 visa (UK Work Permit / UK Work Visa)</b><br>

                        For candidates applying from outside the UK, the Tier 2 (UK Work Permit / UK Work Visa) visa will allow them to live and work in the UK for a maximum of three years plus one month, or the time given in their certificate of sponsorship plus one month, whichever is shorter (beginning no more than 14 days before the start date given on the certificate of sponsorship).<br>

                        For applicants within the UK, who are switching from another visa category, they will able to live and work here for a maximum of three years, or the time given in their certificate of sponsorship plus 14 days, whichever is shorter (beginning on the start date given on the certificate of sponsorship).<br>

                        At the end of 3 years, the Tier 2 Visa (UK Work Permit / UK Work Visa) can be extended for another 2 years. Work permit holders can then apply for settlement after 5 years in the UK.<br>

                        Spouse, common law, civil or unmarried partner of the applicant and any dependent children can be included in the application at the same time. Any such dependents would be granted the same period of leave as the main tier 2 visa applicant. Dependents will be able to work in the UK.<br>

                        <b>Intra-company transfer (ICT) category</b><br>

                        This category is for employees of multinational companies who are being transferred by their overseas employer to a UK branch of the organisation, either on a long-term basis or for frequent short visits. There are 4 sub-categories:

                        <li><b>Long-term staff-</b> for established, skilled employees to be transferred to the UK branch of their organisation for more than 12 months to fill a post that cannot be filled by a new recruit from the resident workforce</li>
                        <li><b>Short-term staff-</b> for established, skilled employees to be transferred to the UK branch of their organisation for 12 months or less to fill a post that cannot be filled by a new recruit from the resident workforce</li>
                        <li><b>Graduate trainee-</b> this route allows the transfer of recent graduate employees to a UK branch of the same organisation, as part of a structured graduate training programme which clearly defines progression towards a managerial or specialist role</li>
                        <li><b>Skills transfer-</b> this route allows the transfer of new graduate employees to a UK branch of the same organisation to learn the skills and knowledge required to perform their job overseas, or to impart their specialist skills or knowledge to the UK workforce.</li><br>
                         <b>Eligibility Criteria for Intra Company Transfer (ICT) Tier 2 Visa</b><br>

                        Applicants applying for an intra-company transfer should have:

                        <li>A licensed sponsor</li>
                        <li>A valid certificate of sponsorship</li>
                        <p>It is a points-based system and applicants are expected to score 50 points in total for attributes listed below:</p>

                        <li><b>Certificate of Sponsorship 30 points</b></li>
                        <li><b>Appropriate salary 20 points</b></li>
                       <br><b> Duration of the Intra Company Transfer (ICT) Tier 2 visa</b><br>

                        The duration of the ICT visa is at the discretion of the sponsoring employer and the particular country’s government. An employer could request for the visa for a few days to up to 5 years. Applicants are obligated to return to their home country once their visa expires.<br>

                        If the visa granted to stay in the UK, the applicant will be able to live and work in the UK for a maximum time of three years, or the time given in their certificate of sponsorship, which ever time is shorter.<br>

                        At the end of this period an extension can be applied for the husband/wife or partner of a candidate may relocate to the UK along with them, although, as the main applicant, the dependents will also need to return to their home country when the permit expires. Dependents can work in the UK.

                        <br><b>Minister of Religion category</b><br>

                        Minister of Religion category Tier 2 (UK Work Permit / UK Work Visa) is for those who wish to go to the UK as religious workers to perform recognized religious duties within religious group for more than 2 years. The duties will include preaching to a community and performing pastoral roles.<br>

                        Applicants under the Tier 2 (Minister of Religion) include those who act in the capacity of a Minister of Religion (either regularly or occasionally), missionaries and members of religious orders. Duties performed by eligible applicants under Tier 2 (Minister of Religion) can include:

                        <li>Leading worship;</li>
                        <li>Preaching or teaching religious education;</li>
                        <li>Officiating at marriages, funerals and other religious services;</li>
                        <li>Offering counselling or welfare support to members of a congregation;</li>
                        <li>Recruiting, training or coordinating the work of local volunteers and preachers; and</li>
                        <li>Organising missionary activities.</li><br>
                        To apply under the Minister of Religion category, you must have:

                        <li>A licensed sponsor</li>
                        <li>A valid certificate of sponsorship.</li><br>
                        It’s a points-based system and points are awarded for meeting the following criteria

                        <li>Certificate of sponsorship – 50 points</li>
                        <li>English language skills – 10 points are awarded. A higher level of English ability is required to address a congregation.</li>
                        <li>Available maintenance (funds) – To earn 10 points in this category, applicants should show personal savings of £800 or written confirmation from their sponsor that the sponsor will maintain and accommodate them until the end of the first month of their work in the United Kingdom</li>
                        <br><b>Sportsperson category</b><br>

                        The sportsperson category under the Tier 2 Visa (UK Work Permit / UK Work Visa) is designed for the elite sports person and coaches who are internationally established at the highest level, and will make a substantial contribution to the development of their sport.<br>

                        To apply under the sportsperson category, you must have:,<br>

                        <li>A licensed sponsor</li>
                        <li>A valid certificate of sponsorship with a governing body endorsement number. The endorsement will confirm the sponsored applicant is internationally established, will make significant contribution for the development of the sport and that the position could not be filled by a resident worker</li>
                        It’s a points-based system and points are awarded for meeting the following criteria

                        <li>Certificate of sponsorship – 50 points</li>
                        <li>English language skills – 10 points are awarded.</li>
                        <li>Available maintenance (funds) – To earn 10 points in this category, applicants should show personal savings of £800 (£533 per dependent) or written confirmation from their sponsor that the sponsor will maintain and accommodate them until the end of the first month of their work in the United Kingdom</li><br>
                        <b>Duration of Stay under</b> Tier 2 Visa (UK Work Permit / UK Work Visa)<br>

                        For candidates applying from outside the UK, this visa will allow them to live and work in the UK for a maximum of three years plus one month, or the time given in their certificate of sponsorship plus one month, whichever is shorter.<br>

                        For applicants within the UK, who are switching from another visa category, they will able to live and work here for a maximum of three years, or the time given in their certificate of sponsorship plus 14 days, whichever is shorter.<br>

                        At the end of 3 years, the visa can be extended for another 2 years, a new Certificate of Sponsorship for his or her continued employment should be issued. After a continuous period of residency in the UK for 5 years, they can apply for settlement as a permanent resident.<br>

                       <b> For more information on visa fee & documents required, contact us!</b>
