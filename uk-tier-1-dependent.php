                    <h3>Marriage / Spouse Visa</h3>
                    <p>Foreign nationals who are already married to a British citizen or person who has settlement status in the UK can enter or remain in the UK on a Spouse/marriage visa. Applicants should apply for entry clearance, if applications are made outside the UK. For those inside the UK, an application for further leave to remain as a spouse should be made.</p>
                    <p>Dependents can take up any employment and there are no restrictions on the type of work that can be undertaken.</p>
                    <p><strong>Eligibility Criteria</strong></p>
                    <p><strong>To apply for the spouse visa the following criteria should be met</strong></p>
                    <li>You are married to or are the civil partner of a person who is present and settled in the United Kingdom;</li>
                    <li>You must be 21 years of age or over and the partner with settled status in the UK must be 21 years of age or over.</li>
                      <li>You are legally married to each other and the marriage is subsisting</li>
                      <li>You and your spouse intend to live together permanently as husband and wife</li>
                      <li>You have met each other</li>
                      <li>Both of you have adequate funds to be able to support yourselves as well as any dependants without claiming public funds.</li>
                      <li>Accommodation for yourselves, and any dependants, must be suitable and available.</li>
                      <li>You must now show that you can speak and understand English if you are a non-European migrant and you want to enter or extend your stay in the UK as the partner of a British citizen or a person settled here.</li>
                      <li>Your spouse/partner must have an income of at least £18,600.</li>
                      <p>If a person has more than one husband or wife, only one of them will be allowed to join them in the UK as their husband or wife under the UK marriage visa.</p>

                      <p>Spouses seeking to come to the UK on the basis of marriage to a UK national should apply for entry clearance before entering the UK. If the couple get married in the UK, it is permissible for the application to be lodged in the UK. If the application is made within the UK, then the person should have leave to remain in the UK on a visa category that was initially issued for a period of more than six months, at the time of making the application.</p>
                    
                    <p><strong>Duration of the Visa</strong></p>

                    <p>Successful applicants are granted permission to stay and work in the UK initially for 2 years. At the end of the 2 year probationary period, a person can apply for Indefinite Leave to Remain (ILR) in the UK, if they are still married and living together.</p>

                    <p>Applicants may also be granted permanent stay in the country; this is usually applicable to a person who has been married and living abroad with their spouse for four years at the time the application is made for entry into the UK.</p>

                    <p>Children under the age of 18 will be granted entry to the UK as dependants and can make their application at the same time as the main applicant. After a total period of three years in UK and if they meet the residence requirements they can apply for British naturalization in order to remain in the country under a grant of UK citizenship.</p>
