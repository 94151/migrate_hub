                  <div id="What Is PESCI (Pre-employment Structured Clinical Interview?" class="tabcontent">
                    <h3>What Is PESCI (Pre-employment Structured Clinical Interview?</h3>
                    <p>
                      The PESCI is an assessment of your clinical experience, knowledge, skills and attributes by an assessment body authorised by the Medical Board of Australia (MBA). The assessment process consists of a structured interview, referee checks and a fee.<br>

The MBA and Australian Health Practitioner Regulation Agency have agreed to implement PESCIs as a mandatory prerequisite for international medical graduates seeking registration in Australia to practice in any of the following roles:<br>

<li>general practice
</li><li>all non-specialist positions in private hospital facilities
</li><li>senior medical officers
</li><li>medical officers (with or without right of private practice)
</li><li>medical superintendents (with or without right of private practice)
</li><li>Your PESCI needs to be arranged by your prospective employer in Australia.</li>
                    </p>
