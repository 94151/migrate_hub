                    <h3>What Is The SPECIALIST ASSESSMENT PROCESS By AMC?</h3>
                    <p>Assessment process is done to verify the educational and practice standards of the applicant in comparison to Australian Medical Standards.

<li>Step 1: Applicant submits the form and documents to AMC

</li><li>Step 2: Application assessed through AMC Primary Source Verification (incomplete application, incomplete documentation makes application invalid for further process)

</li><li>Step 3: Referral sent to Specialist College, applicant advised of referral.

</li><li>Step 4: AMC receives report from specialist medical college advising of applicant’s level of comparability. Report is forwarded to applicant

</li><li>Step 5: AMC receives and notifies the applicant & specialist medical college of conforming to comply with required standards

</li><li>Step 6: AMC receives report from specialist medical college on the applicant’s eligibility & informs the same to applicant</li></p>
