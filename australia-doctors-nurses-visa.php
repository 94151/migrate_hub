                    <h3>Im An Overseas Trained Doctor. How Do I Get A Job In Australia?</h3>
                    <p>Foreign applicants have to get their skills assessed by Australian Medical Council.<br>

Australian Medical Council Computer Adaptive Test (CAT) MCQ (Multiple Choice Questions) Examination assesses your knowledge.<br>

The AMC Computer Adaptive Test (CAT) MCQ Examination is a computer-administered fully integrated multi-choice question examination delivered in one 3.5 hour session in examination centres worldwide.<br>

The examination consists of 150 A-type MCQs (one correct response from five options)—120 scored items and 30 (non-scored) pilot items. You are expected to complete all 150 items and must complete the 120 scored items. Failure to complete all 120 scored items in the examination may lead to insufficient information for a reliable determination of your ability and therefore a result on the AMC adaptive scale. The examination result is recorded as ‘Fail—Insufficient data to obtain result’.<br>
