                    <h3>**Assurance Of Support</h3>
                    <p>Assurance of Support (AoS) is a legal commitment by a person who assures of financial support & pledges that the Parent’s visa holders won’t rely on social welfare payments.<br>

AoS can be given by a person who may or may not be a sponsor.<br>

If under any circumstances, the visa holders avails of any welfare benefits; the person who would have pledged for AoS should repay it<br>

It is also a commitment to repay to the Australian Government certain welfare payments if payments are paid to the assuree during their AoS period.<br>

Assurance of Support’s validity is:

<li>10 years for contributory parent visa holders
</li><li>2 years for all other visa types where an AoS is needed
AoS is considered from:

</li><li>It would be the visa grant date if the applicant is in Australia
</li><li>For applicants who apply from outside Australia, the date would be the date of arrival</li>
                        </p>
