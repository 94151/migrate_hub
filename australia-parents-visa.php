For permanent Parent (including Aged Parent), Aged Dependent Relative and Remaining Relative visa applicants, an AoS bond of AUD 5000 is required for the main applicant. This bond will be held for two years. The AoS bond amount for any adult secondary applicant is AUD 2000.<br>

