                    <p>A points-based system was also introduced for international students wanting to study in the UK. The Tier 4 visa replaces the old UK Study visa.<br>

                      To apply for the UK Student Visa (Tier 4 – UK Study Visa), students will require a university or other qualified UK educational institution to sponsor them. Students are also required to score points in order to be accepted as an international student in the UK.<br>

                      Students coming in to the UK on a student visa will be allowed to work for 20 hours a week during his/her course of study and 40 hours a week during his / her holidays. The student visa is valid till the end of completion of applicant’s course.<br>

                      There are five types of student visas under the Tier 4 points based system<br>

                      <strong>Adult student</strong> UK Student Visa (Tier 4 – UK Study Visa) – This category is for students coming to the UK for post-16 education<br>

                       <strong>Student Visitor</strong> UK Student Visa (Tier 4 – UK Study Visa) – Visitors over 18 years wanting to do a 6 month course in the UK can apply under this category. However, they cannot work nor can they switch into the student visa category while they are in the UK<br>

                       <strong>Child student</strong> UK Student Visa (Tier 4 – UK Study Visa) – Students between the age of 4 -17 can come to the UK to be educated at an independent-fee paying school<br>

                       <strong>Child Visitor</strong> UK Student Visa (Tier 4 – UK Study Visa) – This category is for applicants who are 17 years and over, wanting to do a 6-month course in the UK. However, they cannot work nor can they switch into the student visa category while they are in the UK<br>

                       <strong>Prospective Student</strong> UK Student Visa (Tier 4 – UK Study Visa) – This category is for applicants who want to come into the UK to decide which course to study or to plan a course of study within six months. Applicants are allowed to switch into an adult or child student visa category.</p>

                     
