                    <h3>Adult Student Visa</h3>
                     <p>
                     Students wanting to study a course NQV level 3 or higher in the UK can apply for the UK Student Visa (Tier 4 – UK Study Visa). To be eligible to apply for the student visa, the applicant needs to meet 40 points on the following criteria –

                      <li><b>Sponsorship from approved education provider</b><br>
                      30 points will be granted if the student obtains an offer from a college or university eligible to sponsor. University and colleges should obtain a license to teach international students in the UK. Students should check if the college is on the Tier 4 register of sponsors to earn points.<br>
                      An offer letter also known as a visa letter is issued by the college/university sponsoring you, with information about you, your sponsor, the course, and your finances.</li><br>

                       <li><b
                       >Maintenance Funds</b><br>
                      10 points are awarded to students who show that they have sufficient funds to cover their course fees and monthly living costs.<br>
                      The applicant should possess qualifications equivalent to National Qualification Framework (NQF) in the UK education system<br>
                      The applicant should have a proven track record of his studies before reaching the UK.</li><br>

                      <b>Educational Institutions</b><br>

                      Educational institutions offering courses to overseas students will need to attain the status of approved sponsors. To do so they need to demonstrate that they are bona fide establishments, rather than non-accredited or purely commercial operations.<br>

                      Certificates of sponsorship act as verification from a college or university that students making UK visa applications will abide by the rules and requirements of their course. Educational institutions will also be obliged to report non-enrolment and non-attendance
<br>
                      <b>Basic features of the UK Student Visa (Tier 4 – UK Study Visa)</b><br>

                      <li>Foreign students can gain recognised qualifications from reputable and government approved universities and colleges in the UK</li>
                      <li>Applications can switch from one course to another at the same time, without submitting a new application.</li>
                      <li>Students can undertake limited employment during their study</li>
                      <li>Students can switch into categories like Tier 2 and other categories upon successful completion, without leaving the UK</li>
                      <li>UK Student Visa (Tier 4 – UK Study Visa) can be extended if the course of study is over 4 years</li>
                      <li>Dependents of student visa holders can migrate to the UK</li>
                      <li>Dependents are granted a visa for the same length of time as the applicant</li><br>
                      <b>Duration</b><br>

                      <p>The duration of the UK Student Visa (Tier 4 – UK Study Visa) would depend on the length of course undertaken</p>

                      <li>If the applicant is pursuing a graduate course or above he is permitted to stay for full length of the course, up to 4 years
                      <li>If the course the applicant has undertaken is longer than 4 years then he/she can apply for UK visa extension to complete the course
                      <li>If the applicant is pursuing an undergraduate course he/she will be allowed to stay for a period of 3 years.
                      <li>The applicant who is a child student can stay in the UK will be shortest of the following
