                      There is no provision for dependents to migrate to the UK when the applicant is a child student.<br>

                      Work Rights under the UK Student Visa (Tier 4 – UK Study Visa)<br>

                      The maximum amount of part-time work you can do during term time is:<br>

                      <li>20 hours per week if you are studying a course at or above UK degree level or a foundation degree course, and/or if you made your Tier 4 (General) application on or before 2 March 2010; or</li>
                      <li>10 hours per week if you are studying a course that is below UK degree level and is not a foundation degree course, and you made your Tier 4 (General) application on or after 3 March 2010.</li><br>
