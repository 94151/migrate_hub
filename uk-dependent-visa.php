                    <h3>Fiance / Fiance Visas</h3>
                    <p>A UK Fiance/fiancee visa allows a person to bring their fiance/fiancee to live with them in the UK. The person should have a settled status in the UK and should be currently living in the UK or are returning to the UK with him/her to live permanently.</p>

                    <p><strong>Eligibility Criteria</strong></p>

                    The following requirements must be fulfilled to apply for a fiance/fiancee visa –

                    <li>You are seeking to enter the UK because of your relationship to a person who is present and settled in the UK</li>
                    <li>You and the sponsor are aged 18 and over</li>
                    <li>You plan to marry or register a civil partnership within 6 months</li>
                    <li>You and your fiance/fiancee intend to live together permanently after you are married or registered a civil partnership.</li>
                    <li>You have met your future spouse</li>
                    <li>You are seeking entry for marriage in the UK and not seeking entry as a married person</li>
                    <li>You and your fiance/e must show that there will be sufficient funds and accommodation in the UK without requiring public funds before and after the marriage.</li>
                    <li>Your fiance/e must have an income of at least £18,600.</li>
                    <p>Even if your fiancé, fiancée or proposed civil partner is a national of a country where there is normally no need for a visa to enter the United Kingdom, he/she must obtain permission to enter the United Kingdom before traveling. Applications should be made to the British Diplomatic post in the country the applicant resides in.</p>

                    <p><strong>Duration of the visa</strong></p>

                    <p>Fiances/fiancees will be given entry clearance for a 6-month period, for the purpose of traveling to the UK to get married. During this period they are not allowed to be employed and are prohibited from having access to public funds.</p>

                    <p>If there are exceptional reasons for delaying the marriage until after the initial six months, an extension to stay in the UK may be granted. However you need to demonstrate good reason as to why the marriage has not taken place and be able to show when the marriage will take place.</p>

                    <p>After you have married or registered a civil partnership, the applicant is eligible to apply to stay in the UK as a spouse. The applicant will then be entitled to a 2-year probationary period of leave. Once the fiance/fiancee visa has successfully been switched to a Marriage visa, the applicant will be able to start working in the UK.</p>

                    <p>After two years, if you are still married or in a civil partnership and plan to live together, your husband, wife or civil partner may apply to live permanently in the UK.</p>
