</li><li>He/she should have personal and business assets of AUD1.5 million (AUD400 000 must be net assets in the business), and have a business turnover of AUD3 million
                                                                                    or

</li><li>He/she should have obtained at least AUD1 million in funding from an Australian venture capital firm for a promising high value business idea.</li></p>

                     
