                    <p>Applicant’s Expression of Interest should specify whether he/she is interested in the Significant Business History or the Venture Capital Entrepreneur Stream.<br>

The EOI should also indicate whether he/she is interested in just one or all states and territories.<br>

<b>Note: The visa applicant doesn’t need any supporting documentation while submitting an EOI.</b></p>

                     
                  </div>

                  <div id="Significant Business History Stream" class="tabcontent">
                    <h3>Significant Business History Stream</h3>
                    <p><li>The eligibility for the Significant Business History stream are:

</li><li>Age of the applicant should not be more than 55 years of age (State or territory can waive this requirement if the proposed business is of exceptional economic benefit to the relevant region)
</li><li>Applicant’s net assets should be of at least AUD400 000 (in a qualifying business) in at least 2 of the 4 fiscal years immediately before you are invited to apply
</li><li>Applicant has to legally transfer total assets of at least AUD1.5 million to Australia within 2 years of the visa being granted
</li><li>There should be a total annual turnover of at least AUD3 million (in one or more of your main businesses) in minimum 2 of the 4 fiscal years immediately before you are invited to apply
</li><li>You should hold ownership of minimum:
51 per cent of a business with turnover of less than AUD400 000 per annum, or
30 per cent of a business with turnover of more than AUD400 000 per annum, or
10 per cent of a publicly-listed company
</li><li>You should have an overall successful business career with no involvement in any kind of unacceptable business activities
</li><li>You should have genuine interest to own and maintain a management role in a business in Australia.</li></p>

                     
                  </div>

