<b>Significant Business History Stream</b> is for affluent owners or part owners of a business who want to have a major management role in a new or existing business in Australia.<br>

<b>Venture Capital Entrepreneur Stream</b> is for people who have obtained at least AUD1 million in funding from an Australian venture capital firm.</p>

                     
