                  <br><strong>Invest in Canada</strong><br>

                  If you are looking for a dynamic and prosperous country in which to invest, innovate, grow or expand your business on the world stage, Think Canada!<br>
Canada welcomes foreign business investments and offers many competitive advantages:<br>
Canada is the second best country in the G20 to do business, according to Forbes.<br>
Canada led all G7 countries in economic growth over the past decade (2006–2015).<br>
Once CETA comes into force, foreign investors in Canada will have assured preferential access to both NAFTA and the EU - a vibrant market with a combined GDP of nearly US$37 trillion, or one-half of the world’s output of goods and services.<br>
Canada’s workforce is the most highly educated among members of the OECD, with half of its working-age population having a tertiary level education.<br>
Total business tax costs in Canada are by far the lowest in the G7 and 46 percent lower than those in the United States.
Canada offers the lowest business costs in the G7 for R&D-intensive sectors, with a 27.7 percent cost advantage over the United States.<br>
The World Economic Forum consistently declares Canada’s banking system to be one of the soundest in the world.
Canada is one of the most multicultural countries in the world, with world-class universities, a universal health care system and clean and friendly cities. The OECD’s Better Life Index shows Canada as best in the G7 in terms of overall living conditions and quality of life.

