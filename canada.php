            <br><br>
            <p>Canada is a North American country stretching from the U.S. in the south to the Arctic Circle in the north. Major cities include massive Toronto, west coast film centre Vancouver, French-speaking Montréal and Québec City, and capital city Ottawa. Canada's vast swaths of wilderness include lake-filled Banff National Park in the Rocky Mountains. It's also home to Niagara Falls, a famous group of massive waterfalls.<br>
Canada is a country of opportunities for immigrants. The country extends almost all the privileges of the citizens to the immigrants. The migrants can work and settle in any part of the country. Canada Permanent Residency (PR) can be attained after staying for 3 continuous years in Canada and the family members are also allowed to stay with the immigrant. The country has a low crime rate and offers a high quality life to those with Canada PR status. The new residents are allowed to have the same status, freedom and rights as that of Canadian citizens.
</p>
            
                  

                  <br><p><strong>Migrating to Canada</strong><br>

                  Canada is one of the best countries to migrate to and one of the most welcoming countries. The Govt has devised numerous ways to immigrate to Canada and the most popular one is the Federal Skilled Worker (FSW) Visa. Citizenship and Immigration Canada (CIC) has put forth a cap on the number of applications accepted every year under the Canada FSW Visa.<br>
If your nominated occupation has reached the cap for FSW, you can still immigrate to Canada under the Quebec Skilled Worker Visa. Though this also has a cap on the number of applications accepted per year, it is higher for the Quebec Skilled Worker Visa than the FSW Visa.<br>
If the above does not work then you can even immigrate through the PNP program. These options allow you to apply for a Canadian Permanent Residency down the lane.<br>
A Canadian PR allows you to take any dependent family members with you and in some cases, even allows your partner to work in Canada. The Parent and Grandparent Super Visa allows you can take you parents and grandparents to Canada without any hassle.
<br>

                  <br><strong>Study in Canada</strong><br>

