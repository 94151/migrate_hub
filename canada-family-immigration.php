                    <h3>Relationships Not Eligible For The Family Visa</h3>
                     <p>
                     An applicant would not be eligible to be sponsored as a spouse, a common-law partner or a conjugal partner if:

                      <li>He/she is under 16 years of age</li>
                      <li>He/she or the sponsor was married to someone else at the time of their marriage</li>
                      <li>He/she has stayed apart from the sponsor for a minimum of 1 year and either of them are the common-law or conjugal partner of another person</li>
                      <li>The sponsor has migrated to Canada and, at the time permanent residency was applied for, he/she was a family member who should have been assessed on their eligibility of the immigration requirements, but were not assessed or</li>
                      <li>The sponsor earlier funded another spouse, common-law partner or conjugal partner, and 3 years have not passed since that individual became a Canadian permanent resident.</li><br>
                      If applicant sponsors group of people like spouse and dependent children then all the applications for permanent residency must be filed at the same time. The application for sponsorship and permanent residency must be filed together.<br>

                      If the applicant does not qualify to sponsor/support their spouse, common-law partner or dependent child in the Family Class, their partner/dependent may apply to stay in Canada on humanitarian and compassionate grounds.
                     </p>
                      
                   
