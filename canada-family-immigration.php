                  <div id="Other Eligible Relatives" class="tabcontent">
                    <h3>Other Eligible Relatives</h3>
                    <p>
                    A permanent resident or a citizen of Canada can sponsor certain relatives such as parents and grandparents under the family class program. Both the sponsor and the relative wishing to migrate to Canada must satisfy certain requirements.
                      <p><b>A Canadian resident can sponsor:</b></p>

                      <li>Parents</li>
                      <li>Grandparents</li>
                      <li>Brothers or sisters, nephews or nieces, granddaughters or grandsons who are orphaned, under 18 years of age and not married or in a common-law relationship</li>
                      <li>Children adopted outside of Canada or intended to be adopted in Canada</li>
                      <li>Any other family member can be sponsored regardless of age, if there is no spouse, common-law or conjugal partner, son, daughter, mother, father, brother, sister, grandfather, grandmother, uncle, aunt, niece or nephew who is a Canadian citizen, registered Indian or permanent resident or whom you may sponsor.</li>
                      <li>Accompanying relatives such as spouse, partner and dependent children.</li><br>
                      Relatives over the age of 18, such as brothers and sisters or adult independent children are not eligible and hence cannot be sponsored. However, if they qualify to migrate under the Skilled Worker Class, they may gain additional points for adaptability for having a relative in Canada.</li>

                      <p><b>Parental and Grandparental</b></p>

                      Parents and grandparents can be sponsored under this visa category, by proving their relationship with the sponsor. The sponsor should be willing to live in Canada with the applicant(s) and must be capable of supporting them financially without accessing public funds.<br>

                      The sponsor should meet the required income levels so as to support the applicants coming into Canada. The sponsor has to sign an undertaking to provide for all of the applicants basic needs for the duration of 10 years from the date the applicant arrives in Canada.

                      <p><b>Relatives</b></p>

                      Canadian citizens and permanent residents living in Canada can sponsor one relative regardless of age or relationship, if the Canadian sponsor does not have any relatives who are Canadian citizens or permanent residents or any other relative who may be sponsored under the family category.<br>

                      The Canadian sponsor must provide evidence of sufficient income and must also sign an undertaking to provide for all of the applicant's needs for a specified period of time.
                    </p>
