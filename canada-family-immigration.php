                    <h3>Benefits</h3>
                    <p>Sponsored family members and relatives granted permanent residence under the Family Class will have the right to live, work and study in Canada indefinitely.<br>
It is not a points-based category, but both the sponsor and the relative are expected to meet certain requirements.<br>

Family members entering Canada can work without any restrictions and can avail language training programs and assistance for finding employment<br>
Permanent residents have access to government-funded healthcare, subsidized education and other social care benefits such as Canada Pension Plan Benefits, Old Age Security and Guaranteed Income Supplement.<br>

After three years stay in the country, the applicant can apply for Canadian citizenship.
                        </p>
