                  <div id="Overview" class="tabcontent">
                    <h3>Overview</h3>
                    <p>Applicants who are Canadian nationals or permanent residents can sponsor their husband/wife, conjugal partner, common-law partner, dependent child (including adopted child) or other eligible relative (such as parent or grandparent) to become Canadian permanent resident.<br>

                    Family visas are among the most widely used categories, with over 65,000 family visas issued in 2008. Relatives being sponsored under the family visa category do not need to undergo a points-based assessment.<br>

                    To be eligible to sponsor, the Canadian citizens or residents should be over the age of 18, should be living in Canada and should be willing to sponsor/support their spouse or relatives for a period of 3 to 10 years. The sponsors are also responsible for supporting their relatives financially when they arrives and should ensure their spouse or relative do not seek financial assistance from the government.<br>
                    <p><b>There are two processes for sponsoring a family:</b></p>
                    <li>Spouse, common-law partner and/or dependent children.</li>
                    <li>Eligible relatives.</li>

                    <p><b>Spouse, common-law partner and/or dependent children</b></p>
                    <p>Canadian citizens or residents can bring into Canada as permanent residents their spouse, common-law partner or conjugal partner, or dependent children. The spouse, partner or dependent children could be outside Canada, when the application is made or could be living in Canada.<br>

Both the sponsor and the relative are expected to meet certain requirements. It is the sponsor’s responsibility to support them financially in Canada and do not need to seek monetary assistance from the government.<br>

The sponsor should meet certain income requirements. If the applicant earlier sponsored relatives to come to Canada and they have later on turned for monetary assistance to the government, then the applicant may not be permitted to sponsor one more person.<br>
</p>
<p><b>Eligibility Criteria for a Sponsor</b></p>
<li>The sponsor should be a Canadian Citizen or resident and should be over 18 years of age</li>
<li>A sponsorship agreement should be signed between the sponsor and his/her sponsored relative to commit to providing financial support, if needed. This agreement also states that the person after becoming a permanent resident will make every effort to support him/herself.</li>
<li>Sponsor needs to provide financial support to a spouse, common-law for 3 years from the time they become a permanent resident.</li>
<li>Sponsor needs to provide financial support to a dependent child for 10 years, or until the child turns 25.</li>
<li>If the sponsor resides in Quebec, then he/she need to meet Quebec’s immigration sponsorship requirement, after Citizenship and Immigration Canada (CIC) accepts you as a sponsor.</li>
                    </p>

                     <p><b>Eligibility Criteria for Spouse</b><br>
                     A person can migrate as a spouse if he/she is married to their sponsor and the marriage is officially valid.<br>

                     <li>If a person has married in Canada, they should provide a marriage certificate issued by the province where the marriage took place.</li>
<li>If a person has married outside Canada, then the marriage must be valid under the law of the country where it occurred and under Canadian law. Marriages that happen in an embassy or consulate should be in accordance with the law of the country where it took place, not the country of citizenship of the embassy or consulate.</li>

                     </p>
                     <p><b>Sponsoring same-sex partner as a spouse</b><br>
                     Canadian citizens and residents can apply to sponsor their same-sex partner as a spouse, if they were married in Canada and issued a marriage certificate by a Canadian province on or after the below dates mentioned:<br>

                      <li>British Columbia (on or after July 8, 2003)</li>
                    <li>Manitoba (on or after September 16, 2004)</li>
                    <li>New Brunswick (on or after July 4, 2005)</li>
                    <li>Newfoundland and Labrador (on or after December 21, 2004)</li>
                    <li>Nova Scotia (on or after September 24, 2004)</li>
                    <li>Ontario (on or after June 10, 2003)</li>
                    <li>Quebec (on or after March 19, 2004)</li>
                    <li>Saskatchewan (on or after November 5, 2004)</li>
                    <li>Yukon (on or after July 14, 2004)</li>
                    <li>All other provinces or territories (on or after July 20, 2005).</li><br>
                    If the applicant has married outside Canada, he/she may apply to sponsor their same-sex partner as a spouse provided that the marriage is lawfully accepted according to both the law of the place where the marriage happened and under Canadian law.

                     </p>
                     <p><b>Common-law partner</b><br>
                     Canadian citizens and permanent residents living in Canada can sponsor their common-law partner and eligible dependent children. Applicants who have been residing with their qualifying Canadian partners for a period of 1 year or more in an ongoing relationship can be sponsored under this visa. They need to prove that they are in a genuine and continuing relationship and should have resided together at a common address.<br>

The sponsor and the partner should intend to live together in Canada and an undertaking should be given by the sponsor to provide for all of the applicant's basic needs for a period of 3 years from the partner's arrival to Canada.

                     </p>
                     <p><b>Conjugal partner</b><br>
                     Applicants who have been maintaining an ongoing relationship with their Canadian partner, but have been unable to reside together at a common address with their Canadian partner for a period of at least 1 year, because of exceptional situations beyond their control, can be sponsored under this visa.

                     </p>
                     <p><b>A person may apply as a conjugal partner if:</b><br>
                     He/she has maintained a conjugal relationship with their sponsor for a minimum of one year and has been hindered from staying together or marrying because of:

                      <li>An immigration barrier</li>
                      <li>His/her marital status (for example, he/she is married to someone else and residing in a country where divorce is impossible) or</li>
                      <li>His/her sexual orientation (for example, he/she are in a same-sex relationship and same-sex marriage is not granted where they live)</li>
                      <li>He/she can provide proofs that there were reasons they could not stay together (for example, he/she was refused long-term living in each other’s country).</li><br>
                      The Canadian sponsor must sign an undertaking to provide for the partner's basic needs for the duration of 3 years from the partner's arrival to Canada.

                     </p>
                     <p><b>A person cannot apply as a conjugal partner if:</b><br>
                        <li> He/she could have stayed together but chose not to. This demonstrates they did not have the level of commitment that is needed for a conjugal relationship. (For example, one among both may not have wanted to give up an employment or study, or his/her relationship was not yet at the point where they are ready to live together.)</li>
   <li> He/she could not provide proofs that there was a reason that kept away him/her from living together.</li>
   <li> He/she is engaged to be married. In this case, he/she should either apply as a spouse once the marriage has occurred or apply as a common-law partner if he/she has stayed together continuously for a minimum of 12 months.</li>

                     </p>
                     <p><b>Dependent children</b><br>
                     Canadian citizens and permanent residents living in Canada can apply to sponsor their or their partner's dependent or adopted children. A son or daughter is dependent when the child:

<li>Is below the age of 22 and does not have a spouse or common-law partner</li>
<li>Is a full-time student and is considerably dependent on a parent for financial support since before the age of 22 or</li>
<li>Is financially dependent on a parent since before the age of 22 because of a mental or physical disability</li>

                     </p>
