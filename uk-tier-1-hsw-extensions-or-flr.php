<b>6. Maintenance Funds</b><br>

Applicants in the United Kingdom seeking further leave to remain must have at least £800 (for the principal applicant) of personal savings which must have been held for at least three months prior to the date of application. Additional funds of GBP 533 per dependent should be shown, if the principal applicant has been in the UK for more than 12 months or GBP 1600 per dependent should be shown, if the principal has been in the UK for less than 12 months.
