                    <h3>Overview</h3>
                    <p>A Further Leave to Remain (FLR) application is the process of extending your stay in the UK for a certain period granting you Limited Leave to remain in the UK.<br>

                      A person upon his /her first entrance to the UK will initially be granted an entry clearance from the British Diplomatic Post in their home country for a certain period. To change the visa or extend the visa the applicant will have to make an application for Further Leave to Remain (FLR)<br>

                      There are different types of leave to remain such as student, work permit holder, highly skilled migrant, spouse and dependent. The different forms for different categories of people to extend their further leave application are:<br>

                      <li>Further Leave to Remain (HSMP/Tier 1 Visa extension)</li>
                      <li>Further Leave application (BUS)</li>
                      <li>Further Leave application (BID)</li>
                      <li>Further Leave application (O)</li>
                      <li>Further Leave application (M)</li>
                      <li>Further Leave application (IED)</li>
                      An applicant cannot overstay, the application for further leave to remain should be made before visa expires. An FLR application can be made either by post or in person; an appointment should be taken for applications that are submitted in person. Furthermore, foreign nationals require an identity card.<br>

                      There are specific rules to change the Limited Leave to Remain from one category to another. We will advice on you the right visa application, visa extension and settling down in the UK.</p>
