                    <h3>Overview</h3>
                    <p>Obtaining a Canada Work Permit, Canada Work Visa to Canada is a two step process

<li>The employer has to obtain a letter of confirmation from HRSDC</li>
<li>The employee must apply for Canada Work Permit, Canada Work Visa to Canada consulate</li><br>
The basic requirement is to have an employment offer from a Canadian employer, who should demonstrate that they are unable to find suitable Canadians/permanent residents to fill the jobs and that the entry of these workers will not have a negative impact on the Canadian labour market. The employer has to obtain a letter of confirmation (LOM) from Human Resources and Skills Development Canada (HRSDC) unless exempted. Exemptions exist that allow the applicant to apply directly to CIC for Canada Work Permit, Canada Work Visa without having to obtain letter of confirmation.<br>

Additionally, applicants will also require temporary resident visa to stay in Canada at an overseas Canadian visa office. Work visa holders will be allowed to come and work in Canada for the sponsored employer in the specified role for a period between 6 months to 2 years. They can bring their dependents along with them. Accompanying partners and children may also be eligible to work and study<br>

The work visa will not open a way for permanent stay in Canada instead the applicant has to qualify under an immigration category, such as the skilled worker. Live-in caregivers can stay in Canada permanently if they meet certain requirements.</p>

                     
