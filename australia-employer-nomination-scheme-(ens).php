                  <div id="Types Of Employer Nomination Scheme" class="tabcontent">
                    <h3>Types Of Employer Nomination Scheme</h3>
                    <p>
            There are 3 streams of ENS:<br>

The Temporary Residence Transition stream is for subclass 457 visa holders who have worked for two years and their employer wants to offer them a permanent position.<br>

The Direct Entry stream is for people who have never, or only briefly, worked in the Australian labour market.<br>

The Agreement stream is for people sponsored by an employer through a labour or regional migration agreement.
                    </p>
                    
                  </div>


