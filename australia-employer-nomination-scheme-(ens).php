                   <div id="Requirements" class="tabcontent">
                    <h3>Requirements</h3>
                    <p>Foreign nationals can apply for the ENS if:

They have been nominated by an approved Australian employer
There age is less than 50 years
They meet the skills, qualifications and English language requirements
Abroad professionals can lodge an Expression of Interest (EOI) through Skill Select if they don’t have a nomination from an employer.<br>

Employers and state and territory governments can view the filled details in EOI and decide whether to nominate you for skilled migration.<br>

Australian Government may deny the visa if you owe money to the Australian Government. Arrangements to pay the same can change the decision.<br>

Note: Along with IELTS, Department of Immigration would accept TOEFL iBT and PTE Academic tests from applicants from November 2014</p>

                     
