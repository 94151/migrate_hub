                  <div id="Eligibility" class="tabcontent">
                    <h3>Eligibility</h3>
                     <p>
                     All the Canadian Provinces do not provide Provincial Nomination Schemes; however, most provinces have an agreement with the Government of Canada that allows them to nominate immigrants who wish to settle in that province.<br>

To be eligible to apply for the Provincial Nomination Scheme, the applicants have to pass three stages:<br>

<b>Job Offer</b>– Applicants must have a valid job offer from a Canadian company based in the province to which the candidates intend to migrate. Alternatively, candidates can obtain a nomination certificate if they have a close relative living in that province.
<b>Provincial Government Approval–</b> The job offer should be approved by the relevant provincial government. The applicants are assessed against the migration requirement of the particular province. Candidates will also need to be able to prove that they have a genuine interest to reside and work in the province.
<b>Application for Permanent Residency- </b>Once the applicant receives a nomination from the particular province, they should make their application for permanent residency with Citizenship and Immigration Canada (CIC).
Applicants need to prove that they have enough money to support themselves and their dependants after they arrive in Canada.<br>

Provincial Nominee applications are given preference over other skilled visa applicants by Citizenship and Immigration Canada (CIC).<br>

At present, the following provinces and territories in Canada have Provincial Nomination Schemes:<br>

Alberta
British Columbia
Manitoba
New Brunswick
Newfoundland and Labrador
Nova Scotia
Ontario
Prince Edward Island
Saskatchewan
Yukon</p>
