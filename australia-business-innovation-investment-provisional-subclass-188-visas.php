<li>The Australian Business Innovation stream is for applicants who want to own and manage a new or existing business in Australia.
</li><li>The Australian Investor stream is for applicants who want to make a designated investment in an Australian state or territory and want to maintain business and investment activity in Australia after the original investment has matured.
