Visa is issued only on invitation. The visa applicant has to submit his Expression of Interest through SkillSelect initially and on receipt of Invitation can apply for visa.

This visa allows the applicant to:

Establish a new business or develop an existing business in Australia (the Business Innovation stream)
Make a designated investment with an Australian state or territory government (the Investor stream)
Invest at least AUD5 million in Australia (the Significant Investor stream).</p>

                     
